// Comparison operators - Operadores de comparacion

const numero1 = 20
const numero2 = 50
const numero3 = '20';

console.log(numero1 > numero2); //Greater than
console.log(numero1 < numero2); //Less than
console.log(numero1 == numero3); // Equal - less strict comparator
console.log(numero1 === numero3); // Equal - more strict comparator, compair value and type
console.log(numero1 != numero2); //different
console.log(null == undefined); //Equal - less strict comparator
